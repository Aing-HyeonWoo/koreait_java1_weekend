package com.kita.first.level1;

public class Variable1 {
	public static void main(String[] args) {
		char v1 = 'A';
		
		byte v2;
		short v3;
		int v4 = 3; // 정수형 기본타입
		System.out.println(v4);
		v4 = 5;
		System.out.println(v4);
		long v5 = 214748364234l;
		
		float v6 = 1.2f;
		double v7; // 실수형 기본타입
		
		boolean v8;
		v8 = true;
		v8 = false;
		// 오른쪽에서 왼쪽으로 <---
		String v9;
		
		// 상수
		final int V10; // 스네이크 표기법(전부 대문자)
		V10 = 163; // 다른 값으로 변경 불가 (한번 넣어서 고정)
		final String HAPPY = "안녕";
		

		
		
		
		
		
		
		
		
	}
}
