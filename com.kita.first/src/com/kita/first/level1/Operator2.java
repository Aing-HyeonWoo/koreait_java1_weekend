package com.kita.first.level1;

public class Operator2 {
	public static void main(String[] args) {
		int n1 = +3;
		System.out.println(n1++); // 줄을 수행한 후에 ++
		System.out.println(n1);
		System.out.println(++n1); // 줄을 수행하기 전에 ++
		
		System.out.println(n1--); // 줄을 수행한 후에 --
		System.out.println(n1);
		System.out.println(--n1); // 줄을 수행하기 전에 --
		
		
	}
}
